
# Venv - Sempre use em cada projeto
# Muito bom para fazer o gerenciamento de dependências de módulos
# No requirements.txt

# Para executar um módulo como script
# Para criar é com: 'python -m venv 'nome_da_venv''

# Para ativar: 'source 'nome_da_venv'/bin/active'
# Para desativar: 'deactivate'

# O pip é um instalador de pacotes que busca do Python Package Index, é como o Maven do Java
# Instalar e desinstalar com pip: 'pip install 'pacote'' e 'pip uninstall 'pacote''

# Para atualizar pacotes: 'pip install 'pacote' --upgrade'

# Para mostrar os pacotes: 'pip freeze'
# Para mostrar versões dos pacotes: 'pip index versions 'pacote''

# Para salvar em arquivo os reqs: 'pip freeze > requirements.txt'
# Para baixar do arquivo: ative a venv -> 'pip install -r requirements.txt'